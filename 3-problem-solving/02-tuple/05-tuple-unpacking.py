# Tuple Unpacking

# Given the following tuple:

# student = ("Rejo", 20, "CST")

# Tasks:
# 1. Unpack the tuple into three variables:
#    - name
#    - age
#    - department
# 2. Print all three variables.

# Expected Output:

# Name: Rejo
# Age: 20
# Department: CST

# code ------------------------

student = ("Rejo", 20, "CST")

name, age, department = student

print("Name: ", name)
print("Age: ", age)
print("Department: ", department)