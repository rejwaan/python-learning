# Loop Through a Tuple

# Given the following tuple:

# colors = ("red", "green", "blue", "yellow")

# Tasks:
# 1. Use a for loop to iterate through the tuple.
# 2. Print each element with the text "Color:" before it.

# Expected Output:

# Color: red
# Color: green
# Color: blue
# Color: yellow


# code------------------------

colors = ("red", "green", "blue", "yellow")

for c in colors:
    print("Color: ", c)