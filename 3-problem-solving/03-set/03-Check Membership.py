# Given the following set:

# fruits = {"apple", "banana", "mango", "orange"}

# Tasks:
# 1. Check whether "banana" exists in the set.
# 2. Check whether "grape" exists in the set.
# 3. Print both results.

# Expected Output:

# Banana exists: True
# Grape exists: False


